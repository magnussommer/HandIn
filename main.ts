controller.moveSprite(p, 100, 100)
p.setStayInScreen(true)
info.setScore(0)
info.setLife(3)
level = 1
fallSpeed = 40
game.splash("Level 1", "Plastik")
// --- Spawner (blok-event) ---
game.onUpdateInterval(1000, function () {
    if (randint(0, 99) < 70) {
        t = sprites.create(IMG_PLASTIC, SpriteKind.Food)
        if (level == 1) {
            t.setImage(IMG_PLASTIC)
        } else if (level == 2) {
            t.setImage(IMG_METAL)
        } else {
            t.setImage(IMG_GLASS)
        }
        t.setPosition(randint(10, 150), 0)
        t.vy = fallSpeed
        t.setFlag(SpriteFlag.AutoDestroy, true)
    } else {
        h = sprites.create(IMG_HAZARD, SpriteKind.Enemy)
        h.setPosition(randint(10, 150), 0)
        h.vy = fallSpeed
        h.setFlag(SpriteFlag.AutoDestroy, true)
    }
})
